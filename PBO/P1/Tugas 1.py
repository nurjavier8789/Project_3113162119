#Tugas 1 - Kalkulator sederhana

input1 = int(input("Masukkan angka pertama: "))
input2 = int(input("Masukkan angka kedua: "))

pertambah = input1 + input2
pengurang = input1 - input2
perkali = input1 * input2
pembagi = input1 / input2

print("\nHasil Penjumlahan: " + str(pertambah))
print("Hasil Pengurangan: " + str(pengurang))
print("Hasil Perkalian: " + str(perkali))
print("Hasil Pembagian: " + str(pembagi))
